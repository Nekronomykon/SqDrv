#include "BarLines.h"

#include "EditMarkLines.h"

/////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @name  //
/// @brief //
/// @param //
//
BarLines::BarLines(EditMarkLines *parent)
    : QWidget(parent), ptrHost_(parent)
{
}
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @name  //
/// @brief //
/// @param //
//
QSize BarLines::sizeHint() const
{
    return QSize(ptrHost_->lineNumberAreaWidth(), 0);
}
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @name  //
/// @brief //
/// @param //
//
void BarLines::paintEvent(QPaintEvent *event)
{
    ptrHost_->lineNumberAreaPaintEvent(event);
}
