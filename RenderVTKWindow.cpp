#include "RenderVTKWindow.h"

/////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @name  //
/// @brief //
/// @param //
//
RenderVTKWindow::RenderVTKWindow(vtkRenderWindow *p)
    : ptr_(nullptr)
{
  if (p)
    this->reset(p);
}
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @name  //
/// @brief //
/// @param //
//
RenderVTKWindow::~RenderVTKWindow()
{
  if (ptr_)
    ptr_->Render();
}
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @name  //
/// @brief //
/// @param //
//
vtkRenderWindow *RenderVTKWindow::reset(vtkRenderWindow *p)
{
  std::swap(p, ptr_);
  if (ptr_)
    ptr_->Modified();
  return p;
}
